#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*n;

	n = *alst;
	if (!*alst)
	{
		*alst = new;
		return ;
	}
	while ((*alst)->next)
		*alst = (*alst)->next;
	(*alst)->next = new;
	*alst = n;
}
