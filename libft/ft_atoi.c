#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			signe;
	long int	c;

	signe = 1;
	c = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		c = ((c * 10) + str[i]) - 48;
		i++;
	}
	c = c * signe;
	return (c);
}
