#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;
	size_t	len;

	if (!s)
		return (0);
	i = 0;
	len = ft_strlen((char *)s);
	str = ft_malloc(len + 1, sizeof(char));
	if (!(str))
		return (0);
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
